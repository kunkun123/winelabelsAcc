	LASTUPDATETIME DATE(7),

	TOPENTID VARCHAR2(10),

	BELOWENTID VARCHAR2(10),

	TEMP3 VARCHAR2(200),

	STARTTIME DATE(7),

	TEMP4 VARCHAR2(200),

	TEMP2 VARCHAR2(200),

	CREATOR VARCHAR2(10)
);
