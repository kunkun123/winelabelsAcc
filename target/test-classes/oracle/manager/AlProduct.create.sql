drop table if exists AL_PRODUCT;
create table AL_PRODUCT(
	ID VARCHAR2(10) not null ,

	TEMP4 VARCHAR2(200),

	TEMP3 VARCHAR2(200),

	TEMP2 VARCHAR2(200),

	TEMP1 VARCHAR2(200),

	LASTUPDATETIME DATE(7),

	BATCH VARCHAR2(30),

	CREATEDATE DATE(7),

	CREATOR VARCHAR2(10),

	COLLECTIONTYPE VARCHAR2(10),

	PRODUCTTYPE VARCHAR2(10),

	SPEC VARCHAR2(10),

	PRODUCTNAME VARCHAR2(50),

	PRODUCTLINEID VARCHAR2(10)
);
