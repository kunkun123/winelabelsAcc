drop table if exists MG_PRODUCTLINE;
create table MG_PRODUCTLINE(
	ID VARCHAR2(10) not null ,

	TEMP3 VARCHAR2(200),

	TEMP1 VARCHAR2(200),

	LINENUM VARCHAR2(30),

	TEMP2 VARCHAR2(200),

	NVRIP VARCHAR2(30),

	TEMP4 VARCHAR2(200),

	LASTUPDATE DATE(7),

	STATUS VARCHAR2(10),

	ENTID VARCHAR2(10),

	CREATOR VARCHAR2(10)
);
